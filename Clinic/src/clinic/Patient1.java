    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
       if(PatNameTb.getText().isEmpty() || PatPhoneTb.getText().isEmpty() || IllnessTb.getText().isEmpty() || patAddrressTb.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Missing Information");  
    } else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            
            // Création d'un objet Statement
            PreparedStatement add = con.prepareStatement("insert into patient (nom, telephone, adresse, maladie, genre, dob) values (?,?,?,?,?,?)");
            
            add.setString(1, PatNameTb.getText());
            add.setString(2, PatPhoneTb.getText());
            add.setString(3, patAddrressTb.getText());
            add.setString(4, IllnessTb.getText());
            add.setString(5, GenderTb.getSelectedItem().toString());
            
            // Convert java.util.Date to java.sql.Date
            java.sql.Date sqlDateOfBirth = new java.sql.Date(DOBTb.getDate().getTime());
            add.setString(6, sqlDateOfBirth.toString());

            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Patient Added Successfully");
            con.close();
            DisplayPatients();
            Clear();
        } catch(Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_SaveMouseClicked
