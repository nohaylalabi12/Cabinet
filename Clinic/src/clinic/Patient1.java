    private void EditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditMouseClicked
        if(key == 0 ){
            JOptionPane.showMessageDialog(this, "Select The Patient");
        }
        else{
            try{
                
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
              String Query = "UPDATE mydb.patient SET nom=?, telephone=?, adresse=?, maladie=?, genre=?, dob=? WHERE idpatient=?";
                    PreparedStatement updateStatement = con.prepareStatement(Query);
                    updateStatement.setString(1, PatNameTb.getText());
                    updateStatement.setString(2, PatPhoneTb.getText());
                    updateStatement.setString(3, patAddrressTb.getText());
                    updateStatement.setString(4, IllnessTb.getText());
                    updateStatement.setString(5, GenderTb.getSelectedItem().toString());
                    // Convert java.util.Date to java.sql.Date
                    java.sql.Date sqlDateOfBirth = new java.sql.Date(DOBTb.getDate().getTime());
                    updateStatement.setDate(6, sqlDateOfBirth);
                    updateStatement.setInt(7, key);

                    updateStatement.executeUpdate();

                JOptionPane.showMessageDialog(this, "Patient Updated Successfully");
                DisplayPatients();
                Clear();
            }
            catch(Exception Ex){
                Ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_EditMouseClicked

    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        if(key == 0 ){
        JOptionPane.showMessageDialog(this, "Select The Patient");
    }
    else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            String Query = "DELETE FROM mydb.patient WHERE idpatient=?";
            PreparedStatement deleteStatement = con.prepareStatement(Query);
            deleteStatement.setInt(1, key);

            int result = deleteStatement.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Patient Deleted Successfully");
                DisplayPatients();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete patient");
            }
            DisplayPatients();
            Clear();
            con.close();
        } catch (Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_DeleteMouseClicked

