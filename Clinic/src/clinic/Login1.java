    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
    String username = userName.getText();
    String password = passwordtxt.getText();
    String usertype = userType.getSelectedItem().toString();
    
    Connect();
    try {
        pst = con.prepareStatement("select * from user where  role=? and username=? and password=?");
        pst.setString(1, usertype);
        pst.setString(2, username);
        pst.setString(3, password);

        rs = pst.executeQuery();
        
        if(rs.next()){
            int userid = rs.getInt("iduser");
            String userType = rs.getString("role");
            String userName = rs.getString("username");
            SessionManager.setSession(userid, userName, userType);

            this.setVisible(false);
            if (userType.equals("Doctor")) {
                dashboardDoctor dashboard = new dashboardDoctor(userid, username, userType);
                dashboard.setVisible(true);
            } else if (userType.equals("Nurse")) {
                dashboardNurse dashboard = new dashboardNurse(userid, username, userType);
                dashboard.setVisible(true);
            } else if (userType.equals("Receptionist")) {
                dashboardReceptionist dashboard = new dashboardReceptionist(userid, username, userType);
                dashboard.setVisible(true);
            } else {
                // GÃ©rer d'autres types d'utilisateurs ou afficher un message d'erreur
            }
        } else {
            JOptionPane.showMessageDialog(this, "UserName or Password do not Match! ");
            userName.setText("");
            passwordtxt.setText("");
            userType.setSelectedIndex(-1);
            userName.requestFocus();
        }
    } catch (SQLException ex) {
        Logger.getLogger(Login1.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_loginActionPerformed

