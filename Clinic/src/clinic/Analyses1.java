    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
         if (patAna.getSelectedIndex() == -1|| name.getText().isEmpty() || analyse.getText().isEmpty() || result.getText().isEmpty() ) {
        JOptionPane.showMessageDialog(this, "Missing Information");  
    } else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            // Création d'un objet Statement
            PreparedStatement add = con.prepareStatement("insert into analyses (pat, name,analyse_type, resultat, date_analyse) values (?,?,?,?,?)");
            
            add.setString(1, patAna.getSelectedItem().toString());
            add.setString(2, name.getText());
            add.setString(3, analyse.getText());
            add.setString(4, result.getText());
            java.sql.Date sqlDate = new java.sql.Date(date.getDate().getTime());
            add.setString(5, sqlDate.toString());
            int row = add.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Analyses added successfully");

            con.close();
            DisplayAnalyse();
            Clear();
        } catch(Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_SaveMouseClicked
