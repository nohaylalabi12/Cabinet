private void GetPatient(){
    try{
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
        st = con.createStatement();
        String query = "select * from mydb.patient";
        rs = st.executeQuery(query);
        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
        while(rs.next()){
            String MyPat = rs.getString("nom");
            comboBoxModel.addElement(MyPat);
            
        }
         patAna.setModel(comboBoxModel);
    }catch(Exception Ex){
         Ex.printStackTrace(); 
    }
}

