    private void EditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditMouseClicked
         if(key == 0 ){
            JOptionPane.showMessageDialog(this, "Select The Analyse");
        }
        else{
            try{
                
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
              String Query = "UPDATE mydb.analyses SET pat =?, name=?, type_analyse=?, resultat=?, date_analyse=? WHERE iddocument=?";
                    PreparedStatement updateStatement = con.prepareStatement(Query);
                    updateStatement.setString(1,patAna.getSelectedItem().toString());
                    updateStatement.setString(2, name.getText());
                    updateStatement.setString(3, analyse.getText());
                    updateStatement.setString(4, result.getText());
                    // Convert java.util.Date to java.sql.Date
                   java.sql.Date sqlDate = new java.sql.Date(date.getDate().getTime());
                    updateStatement.setDate(5, sqlDate);
                    
                    updateStatement.setInt(6, key);

                    updateStatement.executeUpdate();

                JOptionPane.showMessageDialog(this, "Analyse Updated Successfully");
                DisplayAnalyse();
                Clear();
            }
            catch(Exception Ex){
                Ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_EditMouseClicked

    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        if(key == 0 ){
        JOptionPane.showMessageDialog(this, "Select The Analyses");
    }
    else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            String Query = "DELETE FROM mydb.analyses WHERE iddocument=?";
            PreparedStatement deleteStatement = con.prepareStatement(Query);
            deleteStatement.setInt(1, key);

            int result = deleteStatement.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Analyses Deleted Successfully");
                DisplayAnalyse();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete Analyses");
            }
            DisplayAnalyse();
            Clear();
            con.close();
        } catch (Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_DeleteMouseClicked

