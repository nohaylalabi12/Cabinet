    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
         if ( cost.getText().isEmpty() || PatName.getText().isEmpty() ) {
        JOptionPane.showMessageDialog(this, "Missing Information");  
    } else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            // Création d'un objet Statement
            PreparedStatement add = con.prepareStatement("insert into payement (name, cost) values (?,?)");
            add.setString(1, PatName.getText());  
            add.setString(2, cost.getText());           
            
            int row = add.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Payement added successfully");

            con.close();
            DisplayPayement();
            Clear();
        } catch(Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_SaveMouseClicked
