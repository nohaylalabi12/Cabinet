    private void EditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditMouseClicked
        if(key == 0 ){
            JOptionPane.showMessageDialog(this, "Select The Payement");
        }
        else{
            try{
                
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
              String Query = "UPDATE mydb.payement SET name=?, cost=? WHERE idpayement=?";
                    PreparedStatement updateStatement = con.prepareStatement(Query);
                    updateStatement.setString(1, PatName.getText());
                    updateStatement.setString(2, cost.getText());
                    updateStatement.setInt(3, key);

                    updateStatement.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Payement Updated Successfully");
                DisplayPayement();
                Clear();
            }
            catch(Exception Ex){
                Ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_EditMouseClicked

    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
            if(key == 0 ){
        JOptionPane.showMessageDialog(this, "Select The Analyses");
    }
    else {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "#Noha@2002#");
            String Query = "DELETE FROM mydb.payement WHERE idpayement=?";
            PreparedStatement deleteStatement = con.prepareStatement(Query);
            deleteStatement.setInt(1, key);

            int result = deleteStatement.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Payement Deleted Successfully");
                DisplayPayement();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete Payement");
            }
            DisplayPayement();
            Clear();
            con.close();
        } catch (Exception Ex) {
            Ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_DeleteMouseClicked

